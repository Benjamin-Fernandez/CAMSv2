import java.util.Scanner;

public class CampAndRole{
    private Camp camp;
    private Role role;

    CampAndRole(Camp camp, Role role) {
        this.camp = camp;
        this.role = role;
    }

    public Camp getCamp() {
        return camp;
    }

    public Role getRole() {
        return role;
    }




}
